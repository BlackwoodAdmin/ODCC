function getFolderIcon(folder) {
  if (folder.type && SYSTEM_ICONS[folder.type]) {
    return SYSTEM_ICONS[folder.type];
  }
  const nameLower = (folder.name || '').toLowerCase();
  if (SYSTEM_ICONS[nameLower]) {
    return SYSTEM_ICONS[nameLower];
  }
  return FOLDER_ICON;
}

export default function FolderSidebar({ folders, activeFolderId, onFolderClick, onCompose }) {
  return (
    <div className="flex flex-col h-full">
      <div className="p-3">
        <button
          onClick={onCompose}
          className="btn-primary w-full flex items-center justify-center gap-2 py-2.5"
        >
          <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24" strokeWidth={2}>
            <path strokeLinecap="round" strokeLinejoin="round" d="M12 4.5v15m7.5-7.5h-15" />
          </svg>
          Compose
        </button>
      </div>

      <nav className="flex-1 overflow-y-auto px-2">
        <ul className="space-y-0.5">
          {(folders || []).map((folder) => {
            const isActive = folder.id === activeFolderId;
            return (
              <li key={folder.id}>
                <button
                  onClick={() => onFolderClick(folder)}
                  className={`w-full flex items-center gap-3 px-3 py-2 rounded-md text-sm transition-colors ${
                    isActive
                      ? 'bg-sage-100 text-sage-700 font-medium'
                      : 'text-charcoal hover:bg-cream-100'
                  }`}
                >
                  <span className={isActive ? 'text-sage-600' : 'text-gray-500'}>
                    {getFolderIcon(folder)}
                  </span>
                  <span className="flex-1 text-left truncate">{folder.name}</span>
                  {folder.unreadCount > 0 && (
                    <span className={`text-xs font-semibold px-1.5 py-0.5 rounded-full min-w-[20px] text-center ${
                      isActive
                        ? 'bg-sage-500 text-white'
                        : 'bg-gray-200 text-gray-600'
                    }`}>
                      {folder.unreadCount}
                    </span>
                  )}
                </button>
              </li>
            );
          })}
        </ul>
      </nav>
    </div>
  );
}
